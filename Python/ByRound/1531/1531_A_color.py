# Problem: CF 1531 A - Зингер | color
# https://codeforces.com/contest/1531/problem/A

"""
Задача: Определить цвет купола Дома Зингера после обработки последовательности сообщений.

Алгоритмы/Техники:
- Симуляция работы бота с учетом состояния блокировки и текущего цвета
- Линейный обход сообщений с обновлением состояния

Временная сложность: O(n), где n - количество сообщений
Пространственная сложность: O(1), используются только константные переменные
"""

a = int(input())  # Чтение количества сообщений
locker = False    # Флаг блокировки изменения цвета, изначально не заблокирован
color = "blue"    # Начальный цвет купола, как указано в условии

for i in range(a):  # Линейный обход всех сообщений
    s = input()     # Чтение очередного сообщения
    
    if s == "lock":     # Обработка команды блокировки
        locker = True   # Установка флага блокировки
        
    elif s == "unlock":  # Обработка команды разблокировки
        locker = False   # Снятие флага блокировки
        
    else:  # Обработка команд смены цвета
        if locker == False:  # Если изменение цвета разрешено
            color = s        # Обновление цвета

print(color)  # Вывод конечного цвета купола


