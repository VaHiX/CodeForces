# Problem: CF 1769 B2 - Копирование файлов II
# https://codeforces.com/contest/1769/problem/B2

"""
Быстрое копирование файлов II
Алгоритмы: Поиск пересечений прогресс-баров
Сложность по времени: O(n * C^2), где n - количество файлов, C = 100
Сложность по памяти: O(C), где C = 100

Описание:
Для каждого файла вычисляются возможные значения, при которых оба прогресс-бара показывают одно и то же число.
Используется метод перебора значений от 0 до 100 и проверка условий на пересечение.
"""

n = int(input())
a = list(map(int, input().split()))
u = sum(a)  # Общая сумма байт всех файлов
C = 100     # Максимальное значение процента
rc = [False for i in range(C + 1)]  # Массив для отслеживания достижимых значений
rc[0] = True   # Ноль всегда достижим
rc[C] = True   # Сто всегда достижим

w = 0  # Накопленный размер файлов до текущего файла
for i in range(n):  # Для каждого файла
    for j in range(1, C):  # Для каждого процента от 1 до 99
        # Вычисляем минимальное количество байт, при котором первый прогресс-бар показывает j%
        x = (j * a[i] + C - 1) // C
        # Вычисляем максимальное количество байт, при котором первый прогресс-бар показывает j%
        y = ((j + 1) * a[i] + C - 1) // C
        # Проверяем, пересекаются ли диапазоны и есть ли совпадение с общим прогресс-баром
        if x < y and C * (w + x) // u <= j and C * (w + y - 1) // u >= j:
            rc[j] = True  # Отмечаем, что значение j достижимо
    w += a[i]  # Добавляем размер копируемого файла к накопленному значению

for i in range(C + 1):  # Выводим все достижимые значения
    if rc[i]:
        print(i)


