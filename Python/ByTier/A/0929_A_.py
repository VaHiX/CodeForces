# Problem: CF 929 A - Прокат велосипедов
# https://codeforces.com/contest/929/problem/A

"""
Проблема: Прокат велосипедов

Алгоритмы: Жадный алгоритм
Сложность времени: O(n)
Сложность пространства: O(1)

Описание:
Аркадий хочет добраться от школы до дома, используя городские велопрокаты.
Каждый велосипед можно использовать не более k км. Велосипеды можно менять в точках проката.
Алгоритм использует жадную стратегию: пока можем ехать на текущем велосипеде до следующего пункта,
продолжаем. Если не можем, меняем велосипед и продолжаем.

Параметры:
n - количество велопрокатов
k - максимальное расстояние, которое можно проехать на одном велосипеде
a - координаты велопрокатов
"""

n, k = map(int, input().split())
a = list(map(int, input().split()))
cur_k = k  # оставшееся расстояние на текущем велосипеде
ans = 0    # количество велосипедов, которые нужно взять
flg = True # флаг для проверки возможности добраться

# Если только два проката, проверяем сразу
if len(a) == 2:
    if a[-1] - a[0] > k:
        flg = False
else:
    # Идем по прокатам с 2-го до предпоследнего
    for i in range(1, n - 1):
        # Если расстояние между двумя соседними прокатами больше k, то не можем проехать
        if a[i] - a[i - 1] > k:
            flg = False
            break
        # Если можем проехать от предыдущего до следующего пункта, используем текущий велосипед
        if a[i + 1] - a[i - 1] <= cur_k:
            cur_k -= a[i] - a[i - 1]
        else:
            # Если нет, меняем велосипед
            cur_k = k
            ans += 1

# Если возможно добраться, выводим количество велосипедов + 1 (первый велосипед)
if flg:
    print(ans + 1)
else:
    print(-1)


