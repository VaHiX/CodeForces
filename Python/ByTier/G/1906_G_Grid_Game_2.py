n = II()


def f(x, y):
    # Base case: if either x or y is zero, the Grundy number is 0
    if x == 0 or y == 0:
        return 0
    # Euclidean algorithm with XOR
    a, b = divmod(x, y)
    return f(y, b) ^ (a & 1)


sg = 0  # Initialize the overall game state (XOR of all cell Grundy numbers)
for _ in range(n):
    r, c = MII()
    # Calculate and XOR the Grundy number for the current cell (r,c)
    # The formula considers 4 adjacent cells (the cell itself and 3 others) 
    # to compute the effective contribution of this cell to the game state
    sg ^= f(r, c) ^ f(r - 1, c) ^ f(r, c - 1) ^ f(r - 1, c - 1)

# Output result: if sg is 0, second player wins; otherwise first player wins
print(["SECOND", "FIRST"][sg])


