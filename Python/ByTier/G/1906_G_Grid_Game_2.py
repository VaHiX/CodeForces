"""
Algorithm: Game Theory (Nim-like Game)
This code solves a combinatorial game where players take turns manipulating a grid of black and white cells.
The game is modeled as a Nim-like game using the Sprague-Grundy theorem.
Each cell contributes to the overall game state based on its coordinates.
The Grundy number (nimber) for a cell is calculated using the XOR of Grundy numbers for adjacent cells.
The overall game state is determined by XORing all individual cell Grundy numbers.

Time Complexity: O(N) where N is the number of black cells
Space Complexity: O(1) as we only use a constant amount of extra space for calculations

The key insight is to compute the Grundy number for each cell using a recursive function with memoization,
but since we're calculating it once per cell and the recursion is limited (due to Euclidean algorithm nature),
the complexity remains linear in terms of number of cells.
"""

