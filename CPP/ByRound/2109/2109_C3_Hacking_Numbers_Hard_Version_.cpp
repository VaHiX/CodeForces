#include <bits/stdc++.h>
using namespace std;
int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  int t, n, r;
  cin >> t;
  while (t--) {
    cin >> n;
    cout << "mul 999999999" << endl;
    cin >> r;
    cout << "digit" << endl;
    cin >> r;
    if (n != 81) {
      cout << "add " << n - 81 << endl;
      cin >> r;
    }
    cout << "!" << endl;
    cin >> r;
  }
  return 0;
}
