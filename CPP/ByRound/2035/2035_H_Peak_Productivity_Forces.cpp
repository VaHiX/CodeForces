#include <bits/stdc++.h>

using namespace std;

using ll = long long;
const int MOD = 1E9 + 7;
const int INF = 1E9;
const ll INFLL = 1E18;

int n;
vector<int> p;
vector<int> q;
int delta;
vector<int> wochien;

void shift(int x) {
  wochien.push_back(x);
  delta++;
  int temp = p[x - delta + 1 + 2 * n];
  int temp2 = -1;
  int temp3 = -1;
  q[p[x - delta + 1 + 2 * n]] = x - delta + 2 * n;
  int tl = -1;
  if (x) {
    q[p[x - delta + 2 * n]] = -delta + 2 * n;
    temp2 = p[x - delta + 2 * n];
  }
  if (x + 1 < n) {
    q[p[n - delta + 2 * n]] = (x + 1) - delta + 2 * n;
    temp3 = p[n - delta + 2 * n];
  }
  p[x - delta + 2 * n] = temp;
  if (x) {
    p[-delta + 2 * n] = temp2;
  }
  if (x + 1 < n) {
    p[(x + 1) - delta + 2 * n] = temp3;
  }
}

int getPos(int x) { return q[x] + delta - 2 * n; }

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  int T;
  cin >> T;
  for (int test = 1; test <= T; test++) {
    cin >> n;
    vector<int> a(n);
    vector<int> b(n);
    for (int i = 0; i < n; i++) {
      cin >> a[i];
      a[i]--;
    }
    for (int i = 0; i < n; i++) {
      cin >> b[i];
      b[i]--;
    }
    if (n == 1) {
      cout << "0\n";
      continue;
    } else if (n == 2) {
      if (a == b) {
        cout << "0\n";
      } else {
        cout << "-1\n";
      }
      continue;
    }
    vector<int> pos(n);
    for (int i = 0; i < n; i++) {
      pos[a[i]] = i;
    }
    delta = 0;
    p.resize(3 * n);
    q.resize(n);
    for (int i = 0; i < n; i++) {
      p[pos[b[i]] + 2 * n] = i;
      q[i] = pos[b[i]] + 2 * n;
    }
    wochien.clear();
    if (getPos(n - 1) && getPos(n - 1) != n - 1) {
      shift(getPos(n - 1) + 1);
    } else if (getPos(n - 1)) {
      shift(n - 3);
      shift(n - 1);
    }
    for (int i = n - 2; i >= 0; i--) {
      if (i == 2 && getPos(2) == n - 1 && getPos(1) == n - 2 &&
          getPos(0) == n - 3) {
        shift(n - 1);
        shift(0);
        shift(0);
        break;
      } else if (i == 2 && getPos(2) == n - 1 && getPos(1) == n - 3 &&
                 getPos(0) == n - 2) {
        shift(n - 2);
        shift(0);
        shift(0);
        break;
      } else if (i == 1 && getPos(1) == n - 2 && getPos(0) == n - 1) {
        shift(n - 1);
        shift(0);
        break;
      } else if (getPos(i) != n - 1) {
        shift(getPos(i) + 1);
      } else {
        if (getPos(i - 1) != n - 2 && getPos(i - 1) != n - 3) {
          shift(getPos(i - 1) + 1);
          shift(getPos(i) + 1);
        } else {
          shift(getPos(i - 1) + 1);
          shift(0);
        }
        i--;
      }
    }
    for (int i = n - 2; i >= 0; i--) {
      if (getPos(i) == n - 2) {
        shift(n - 1);
      } else {
        shift(n - 2);
      }
    }
    cout << wochien.size() << "\n";
    for (int i : wochien) {
      cout << i + 1 << " ";
    }
    cout << "\n";
  }
}